package com.mr_trying.companion.Models;

public class Flight {

    private String flightNum;
    private String fromStation;
    private String toStation;
    private String sendingDate;
    private String arrivalDate;
    private String startLat;
    private String startLng;
    private String endLat;
    private String endLng;

    public Flight(String flightNum, String fromStation, String toStation,
                  String sendingDate, String arrivalDate, String startLat, String startLng, String endLat, String endLng) {
        this.flightNum = flightNum;
        this.fromStation = fromStation;
        this.toStation = toStation;
        this.sendingDate = sendingDate;
        this.arrivalDate = arrivalDate;
        this.startLat = startLat;
        this.startLng = startLng;
        this.endLat = endLat;
        this.endLng = endLng;
    }

    public Flight() {
    }

    public String getFlightNum() {
        return flightNum;
    }

    public void setFlightNum(String flightNum) {
        this.flightNum = flightNum;
    }

    public String getFromStation() {
        return fromStation;
    }

    public void setFromStation(String fromStation) {
        this.fromStation = fromStation;
    }

    public String getToStation() {
        return toStation;
    }

    public void setToStation(String toStation) {
        this.toStation = toStation;
    }

    public String getSendingDate() {
        return sendingDate;
    }

    public void setSendingDate(String sendingDate) {
        this.sendingDate = sendingDate;
    }

    public String getArrivalDate() {
        return arrivalDate;
    }

    public void setArrivalDate(String arrivalDate) {
        this.arrivalDate = arrivalDate;
    }

    public String getStartLat() {
        return startLat;
    }

    public void setStartLat(String startLat) {
        this.startLat = startLat;
    }

    public String getStartLng() {
        return startLng;
    }

    public void setStartLng(String startLng) {
        this.startLng = startLng;
    }

    public String getEndLat() {
        return endLat;
    }

    public void setEndLat(String endLat) {
        this.endLat = endLat;
    }

    public String getEndLng() {
        return endLng;
    }

    public void setEndLng(String endLng) {
        this.endLng = endLng;
    }
}
